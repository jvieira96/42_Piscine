#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	x;

	i = 1;
	x = 0;
	while (argc > i)
	{
		while (argv[i][x] != '\0')
		{
			write(1, &argv[i][x], 1);
			x++;
		}
		write(1, "\n", 1);
		i++;
		x = 0;
	}
	return (0);
}

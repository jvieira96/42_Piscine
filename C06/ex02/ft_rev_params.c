#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	x;
	int	i;

	i = argc - 1;
	x = 0;
	while (i > 0)
	{
		while (argv[i][x] != '\0')
		{
			write(1, &argv[i][x], 1);
			x++;
		}
		x = 0;
		i--;
		write (1, "\n", 1);
	}
	return (0);
}
